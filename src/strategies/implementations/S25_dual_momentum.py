import sys, os
sys.path.insert(0, os.path.join(os.path.dirname(os.path.abspath(__file__)), '..', '..'))
from strategies.base import BaseStrategy, Signal


class DualMomentum(BaseStrategy):
    id                = 'S25_dual_momentum'
    name              = 'Dual Momentum (12-1mo, Top7)'
    description       = '12-1 month cross-sectional momentum with absolute momentum filter and top-7 concentration'
    tier              = 2
    signal_frequency  = 'daily'
    min_lookback      = 273  # 252 + 21
    active_in_regimes = ['LOW_VOL', 'TRANSITIONING']
    data_requirements = ['prices']

    def default_parameters(self) -> dict:
        return {
            'mom_window':   252,   # 12-month lookback
            'skip_window':  21,    # skip last month (short-term reversal avoidance)
            'top_n':        7,     # concentrate in top 7 winners
        }

    def generate_signals(self, prices, regime, universe, aux_data=None):
        """
        Jegadeesh-Titman cross-sectional momentum with Antonacci dual-momentum filter.
        Relative momentum: rank SP100 by 12-1 month return, take top 7.
        Absolute momentum: each position must have positive trailing return (no shorting losing names).
        Regime gate: only active in LOW_VOL and TRANSITIONING regimes.
        """
        p = self.parameters
        if not self.should_run(regime.get('state', 'LOW_VOL')):
            return []
        if prices is None or len(prices) < self.min_lookback:
            return []

        scale     = self.position_scale(regime.get('state', 'LOW_VOL'))
        mom_w     = p['mom_window']
        skip_w    = p['skip_window']
        top_n     = p['top_n']
        available = [t for t in universe if t in prices.columns]

        # ── SIGNAL LOGIC ───────────────────────────────────────────────────────
        moms = {}
        for ticker in available:
            ts = prices[ticker].dropna()
            if len(ts) < mom_w:
                continue
            start_val = float(ts.iloc[-mom_w])
            end_val   = float(ts.iloc[-skip_w])
            if start_val <= 0:
                continue
            m = end_val / start_val - 1
            if m > 0:  # absolute momentum filter
                moms[ticker] = m

        if not moms:
            return []

        winners = pd.Series(moms).sort_values(ascending=False).head(top_n)
        pos_size = round((1.0 / top_n) * scale, 4)

        signals = []
        for ticker in winners.index:
            ts      = prices[ticker].dropna()
            current = float(ts.iloc[-1])
            stops   = self.compute_stops_and_targets(ts, 'LONG', current)
            signals.append(Signal(
                ticker            = ticker,
                direction         = 'LONG',
                entry_price       = current,
                stop_loss         = stops['stop'],
                target_1          = stops['t1'],
                target_2          = stops['t2'],
                target_3          = stops['t3'],
                position_size_pct = pos_size,
                confidence        = 'MED',
                signal_params     = {
                    'momentum_12_1': round(float(moms[ticker]), 4),
                    'rank':          int(list(winners.index).index(ticker) + 1),
                },
            ))

        return signals
