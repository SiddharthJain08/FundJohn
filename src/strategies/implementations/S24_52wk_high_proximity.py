import sys; sys.path.insert(0, 'src/strategies')
from base import BaseStrategy, Signal


class FiftyTwoWeekHighProximityStrategy(BaseStrategy):
    id               = 'S24_52wk_high_proximity'
    name             = '52-Week High Proximity Momentum'
    description      = 'Buy top stocks closest to their 52-week high (>=92% proximity). Monthly rebalance. George & Hwang behavioral anchor anomaly.'
    tier             = 2
    signal_frequency = 'monthly'
    min_lookback     = 260
    active_in_regimes = ['LOW_VOL', 'TRANSITIONING']

    def default_parameters(self):
        return {
            'hold_days':     21,
            'n_long':        2,
            'lookback_days': 252,
            'min_proximity': 0.92,
        }

    def generate_signals(self, prices, regime, universe, aux_data=None):
        """
        52-Week High Proximity Momentum strategy.

        Signal: rank SP100 stocks by ratio (current_price / 52-week-high).
        Buy top N stocks that are within min_proximity of their 52-week high.

        Economic rationale: George & Hwang (2004) document that stocks near their
        52-week high experience underreaction — investors anchor to the prior high
        as a reference point, causing them to be reluctant to buy 'above' it.
        This creates persistent upward momentum in stocks approaching or re-testing
        their highs. Monthly rebalance avoids excessive costs. Natural de-risking:
        during crashes, stocks fall from highs → dropped from portfolio automatically.

        Risk management: the 92% proximity filter excludes laggards, ensuring we
        only hold confirmed uptrending names. MaxDD naturally bounded because strategy
        exits when stocks fall away from their highs.
        """
        p     = self.parameters
        state = regime.get('state', 'LOW_VOL')
        scale = self.position_scale(state)

        if not self.should_run(state):
            return []

        if prices is None or len(prices) < self.min_lookback:
            return []

        lookback  = p['lookback_days']
        n_long    = p['n_long']
        min_prox  = p['min_proximity']
        cols      = [t for t in universe if t in prices.columns and t != 'SPY']

        if len(cols) < n_long:
            return []

        window   = prices[cols].iloc[-lookback:]
        current  = window.iloc[-1]
        hi_52wk  = window.max()
        ratio    = (current / hi_52wk).replace([float('inf'), float('-inf')], float('nan')).dropna()
        ratio    = ratio[ratio >= min_prox]

        if len(ratio) < 1:
            return []

        top = ratio.sort_values(ascending=False).head(n_long)
        signals = []

        for ticker in top.index:
            if ticker not in prices.columns:
                continue
            ts      = prices[ticker].dropna()
            if len(ts) < 14:
                continue
            current_price = float(ts.iloc[-1])
            hi            = float(prices[ticker].iloc[-lookback:].max())
            proximity     = current_price / hi

            stops = self.compute_stops_and_targets(ts, 'LONG', current_price)
            signals.append(Signal(
                ticker            = ticker,
                direction         = 'LONG',
                entry_price       = current_price,
                stop_loss         = stops['stop'],
                target_1          = stops['t1'],
                target_2          = stops['t2'],
                target_3          = stops['t3'],
                position_size_pct = round(0.15 * scale, 4),
                confidence        = 'MED' if proximity >= 0.97 else 'LOW',
                signal_params     = {
                    '52wk_high':  round(hi, 2),
                    'proximity':  round(proximity, 4),
                    'pct_from_high': round((1 - proximity) * 100, 2),
                },
            ))

        return signals
