import pandas as pd
import numpy as np
import sys; sys.path.insert(0, 'src/strategies')
from base import BaseStrategy, Signal


class RegimeMomentumStrategy(BaseStrategy):
    id               = 'S23_regime_momentum'
    name             = 'Regime-Conditioned Momentum'
    description      = 'SPY realized vol as regime detector. Low vol→63d momentum top2. High vol→lowest vol defensive top2. Monthly rebalance.'
    tier             = 2
    signal_frequency = 'monthly'
    min_lookback     = 70
    active_in_regimes = ['LOW_VOL', 'TRANSITIONING', 'HIGH_VOL']

    def default_parameters(self):
        return {
            'hold_days':      21,
            'n_long':         2,
            'mom_days':       63,
            'vol_days':       20,
            'spy_vol_days':   20,
            'lo_vol_thresh':  0.15,
            'hi_vol_thresh':  0.28,
        }

    def generate_signals(self, prices, regime, universe, aux_data=None):
        """
        Regime-conditioned monthly momentum.
        Low vol regime (SPY 20d RV < 15%): top 2 stocks by 63d momentum (price continuation).
        High vol regime (SPY 20d RV > 28%): top 2 stocks by lowest realized vol (defensive).
        Transitioning: blend of both.

        Economic rationale: Momentum works in low-vol trending markets (Jegadeesh & Titman 1993).
        Fails in vol spikes. Vol-conditioning removes known failure mode. Monthly rebalance
        avoids excessive transaction costs. Concentrating in top 2 maximizes alpha per unit risk.
        """
        p      = self.parameters
        state  = regime.get('state', 'LOW_VOL')
        scale  = self.position_scale(state)

        if not self.should_run(state):
            return []

        if prices is None or len(prices) < self.min_lookback:
            return []

        cols = [t for t in universe if t in prices.columns and t != 'SPY']

        # SPY realized vol as inline regime sensor
        spy_rv = 0.18  # default TRANSITIONING
        if 'SPY' in prices.columns:
            spy_rets = prices['SPY'].pct_change().dropna()
            if len(spy_rets) >= p['spy_vol_days']:
                spy_rv = float(spy_rets.iloc[-p['spy_vol_days']:].std() * np.sqrt(252))

        lo_thresh = p['lo_vol_thresh']
        hi_thresh = p['hi_vol_thresh']
        n_long    = p['n_long']
        mom_days  = p['mom_days']
        vol_days  = p['vol_days']

        if spy_rv < lo_thresh:
            # LOW VOL: momentum
            mom = prices[cols].iloc[-mom_days:].apply(
                lambda x: (x.dropna().iloc[-1] / x.dropna().iloc[0] - 1)
                          if len(x.dropna()) > mom_days // 2 else np.nan
            )
            picked = mom.dropna().sort_values(ascending=False).head(n_long).index.tolist()

        elif spy_rv > hi_thresh:
            # HIGH VOL: defensive — lowest realized vol
            rvol  = prices[cols].pct_change().iloc[-vol_days:].std() * np.sqrt(252)
            picked = rvol.dropna().sort_values(ascending=True).head(n_long).index.tolist()

        else:
            # TRANSITIONING: mix
            mom   = prices[cols].iloc[-mom_days:].apply(
                lambda x: (x.dropna().iloc[-1] / x.dropna().iloc[0] - 1)
                          if len(x.dropna()) > mom_days // 2 else np.nan
            )
            rvol  = prices[cols].pct_change().iloc[-vol_days:].std() * np.sqrt(252)
            mo_k  = max(1, n_long // 2)
            vl_k  = n_long - mo_k
            mo_pick = mom.dropna().sort_values(ascending=False).head(mo_k).index.tolist()
            vl_pick = rvol.dropna().sort_values(ascending=True).head(vl_k).index.tolist()
            picked  = list(set(mo_pick + vl_pick))[:n_long]

        signals = []
        for ticker in picked:
            if ticker not in prices.columns:
                continue
            ts      = prices[ticker].dropna()
            if len(ts) < 14:
                continue
            current = float(ts.iloc[-1])
            stops   = self.compute_stops_and_targets(ts, 'LONG', current)
            signals.append(Signal(
                ticker            = ticker,
                direction         = 'LONG',
                entry_price       = current,
                stop_loss         = stops['stop'],
                target_1          = stops['t1'],
                target_2          = stops['t2'],
                target_3          = stops['t3'],
                position_size_pct = round(0.15 * scale, 4),
                confidence        = 'MED',
                signal_params     = {
                    'spy_rv':      round(spy_rv, 4),
                    'regime_mode': 'momentum' if spy_rv < lo_thresh else 'defensive' if spy_rv > hi_thresh else 'blend',
                },
            ))

        return signals
