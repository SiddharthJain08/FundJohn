import pandas as pd
import numpy as np
from ..base import BaseStrategy, Signal


class JTMomentum12Mo(BaseStrategy):
    id               = 'jt_momentum_12mo'
    name             = 'JT 12-Month Momentum (Top 5)'
    description      = 'Long top 5 SP100 names by 12mo (skip 1mo) cross-sectional momentum'
    tier             = 2
    signal_frequency = 'daily'
    min_lookback     = 273          # 252 lookback + 21 skip
    active_in_regimes = ['LOW_VOL', 'TRANSITIONING', 'HIGH_VOL']

    def default_parameters(self) -> dict:
        return {
            'lookback_long': 252,   # 12-month lookback window (trading days)
            'lookback_skip': 21,    # skip most recent month to avoid reversal
            'top_n':         5,     # number of positions
            'min_mom_thresh': 0.05, # minimum 12mo return to qualify (filter losers)
        }

    def generate_signals(self, prices, regime, universe, aux_data=None):
        """
        Rank universe by 12-month (skip 1-month) price momentum.
        Go long the top N names. Exit when rank falls below top 2*N.
        """
        p = self.parameters

        # Regime gate
        if not self.should_run(regime.get('state', 'LOW_VOL')):
            return []

        if prices is None or len(prices) < self.min_lookback:
            return []

        # Filter to tradeable equities only — exclude indices, futures, crypto
        available = [
            t for t in universe
            if t in prices.columns
            and not t.startswith('^')
            and not t.endswith('=F')
            and '-USD' not in t
        ]
        if len(available) < 10:
            return []

        prices_u = prices[available].copy()

        # Compute 12mo-1mo momentum: return from day[-273] to day[-21]
        lookback = p.get('lookback_long', 252)
        skip     = p.get('lookback_skip', 21)
        top_n    = p.get('top_n', 5)
        min_mom  = p.get('min_mom_thresh', 0.05)

        if len(prices_u) < lookback + skip:
            return []

        price_start = prices_u.iloc[-(lookback + skip)]
        price_end   = prices_u.iloc[-skip]

        mom = (price_end / price_start - 1).dropna()
        mom = mom[mom >= min_mom]  # minimum positive momentum required

        if len(mom) < 3:
            return []

        # Top N by momentum
        top_names = mom.nlargest(top_n).index.tolist()

        scale    = self.position_scale(regime.get('state', 'LOW_VOL'))
        pos_size = round((1.0 / top_n) * 0.15 * scale, 4)  # ~15% max total exposure

        signals = []
        for ticker in top_names:
            ts = prices_u[ticker].dropna()
            if len(ts) < 14:
                continue

            current = float(ts.iloc[-1])
            stops   = self.compute_stops_and_targets(ts, 'LONG', current)

            ticker_mom = float(mom.get(ticker, 0.0))
            mom_rank   = float(mom.rank(pct=True).get(ticker, 0.5))

            # Confidence based on momentum rank: top 10% = HIGH, 10-30% = MED, else LOW
            if mom_rank >= 0.90:
                confidence = 'HIGH'
            elif mom_rank >= 0.70:
                confidence = 'MED'
            else:
                confidence = 'LOW'

            signals.append(Signal(
                ticker            = ticker,
                direction         = 'LONG',
                entry_price       = current,
                stop_loss         = stops['stop'],
                target_1          = stops['t1'],
                target_2          = stops['t2'],
                target_3          = stops['t3'],
                position_size_pct = pos_size,
                confidence        = confidence,
                signal_params     = {
                    'momentum_12mo':  round(ticker_mom, 4),
                    'momentum_rank':  round(mom_rank, 4),
                    'lookback_days':  lookback,
                    'skip_days':      skip,
                    'regime':         regime.get('state', 'UNKNOWN'),
                    'scale':          scale,
                },
            ))

        return signals
